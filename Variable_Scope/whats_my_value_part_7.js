/**
 * What will the following code log to the console and why? Don't run it until
 * you have tried to answer.
 */

let a = 1;

function myFunction() {
  console.log(a);
}

myFunction();

/**
 * Solution
 * The above code will log `1` to the console. This demonstrates how any
 * variable defined in an outer scope is accessible from within an inner scope.
 */